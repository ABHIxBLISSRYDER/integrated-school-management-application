// Sidebar menu component
const Menu = async () => {
  const user = await currentUser(); // Get the currently authenticated user
  const role = user?.publicMetadata.role as string; // Extract user's role

  return (
    <div className="mt-4 text-sm">
      {menuItems.map((section) => (
        <div className="flex flex-col gap-2" key={section.title}>
          {/* Section title (only visible on large screens) */}
          <span className="hidden lg:block text-gray-400 font-light my-4">
            {section.title}
          </span>

          {/* Render only menu items visible to current user's role */}
          {section.items.map((item) => {
            if (item.visible.includes(role)) {
              return (
                <Link
                  key={item.label}
                  href={item.href}
                  className="flex items-center justify-center lg:justify-start gap-4 text-gray-500 py-2 md:px-2 rounded-md hover:bg-SkyLight"
                >
                  <Image src={item.icon} alt="" width={20} height={20} />
                  <span className="hidden lg:block">{item.label}</span>
                </Link>
              );
            }
          })}
        </div>
      ))}
    </div>
  );
};

